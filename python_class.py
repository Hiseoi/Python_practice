"""Television 리모콘과 같은 기능을 만들어보자"""

class Television():
    def __init__(self, volume, power):
        self.volume=volume
        self.power=power
        
    def show(self):
        print(self.volume,self.power)
    
    def max(self):
        self.volume=100
        
    def min(self):
        self.volume=0
        
    def mute(self):
        self.volume='mute'
        
    def on(self):
        self.power=True
        
    def off(self):
        self.power=False

TV=Television(5,True)

TV.volume

TV.max()

TV.volume

TV.off()

TV.power

TV.show()

