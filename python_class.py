class Calculator:
    def __init__(self):
        self.value=0
        
    def add(self,number):
        self.value+=number
        
    def sub(self,number):
        self.value-=number
        
    def mul(self,number):
        self.value*=number
        
    def div(self,number):
        self.value/=number

C=Calculator()
C.add(3)
C.add(2)
C.sub(1)
C.mul(3)
C.div(6)
print(C.value)

